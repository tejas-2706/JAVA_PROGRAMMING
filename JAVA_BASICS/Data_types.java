public class Data_types {
    public static void main(String args[]){

        /*SIZE OF DATA TYPES
         * 1 bytes == 8 bits
         */
/*storage of data types as follows;
 * byte = 1 bytes [-128 to 128]
 * short = 2 bytes
 * char = 2 bytes
 * boolean = 1 bytes
 * int = 4 bytes [-2B to 2B]
 * long = 8 bytes
 * float =4 bytes
 * double = 8 bytes
 * 
 */
        byte a = 5;                       
        System.out.println(a);
        int number = 430;
        System.out.println(number);
        char ch = 'a';
        System.out.println(ch);
        long ln = 547;
        System.out.println(ln);
        float price = 10;
        System.out.println(price);
        double pi = 3.14;
        System.out.println(pi);
        boolean bl = true;
        System.out.println(bl);
        short sh = 22;
        System.out.println(sh);
    }
    
}
