public class Operators {
    public static void main(String args []){
        // Logical Operators-----------------        
        int A = 10;
        int B = 5;
        System.out.println((A>B) && (A<B));
        System.out.println((A<B) || (A>B));
        System.out.println( ! (A<B));

    }
    
}
